import SwiftUI

extension Localization {
    enum LoginOptions {
        static var title: LocalizedStringKey { return "loginOptions.title" }
        static var description: LocalizedStringKey { return "loginOptions.description" }
        static var loginEmail: LocalizedStringKey { return "loginOptions.login.email" }
        static var loginPhone: LocalizedStringKey { return "loginOptions.login.phone" }
    }
}

